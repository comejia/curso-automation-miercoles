package com.automation.clase2;

public class P2Datos {

    public static void main(String[] args) {
        // PRACTICA 2
        System.out.println("Hello and welcome!"); // esto es una cadena de texto (string)

        System.out.println(10); // esto un numero del tipo entero (int)

        System.out.println(25.45); // esto un numero con decimales (float)

        System.out.println('h'); // esto es un caracter (char)

        System.out.println(true); // esto es un boolean (true/false)
        System.out.println(false); // esto es un boolean
    }
}
